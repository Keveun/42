char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	if (!src[0])
		return (dest);
	j = 0;
	while (dest[j])
		j++;
	i = 0;
	while (src[i])
	{
		dest[i + j] = src[i];
		i++;
	}
	dest[i + j] = '\0';
	return (dest);
}
