char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	if (str[1] >= 'A' && str[1] <= 'Z')
		str[1] += 32;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 'a' && str[i] <= 'z') ||
			(str[i] >= '0' && str[i] <= '9') ||
			(str[i] >= 'A' && str[i] < 'Z')))
		{
			if (str[i + 1] >= 'a' && str[i + 1] <= 'z')
				str[i + 1] -= 32;
		}
		if (str[i + 2] >= 'A' && str[i + 2] < 'Z')
			str[i + 2] += 32;
		i++;
	}
	return (str);
}
