int		match(char *s1, char *s2)
{
	if (!*s2)
		return (!*s1);
	if (*s2 == '*')
		return (match(s1, s2 + 1) || (*s1 && match(s1 + 1, s2)));
	return ((*s1 == *s2) && match(s1 + 1, s2 + 1));
}
